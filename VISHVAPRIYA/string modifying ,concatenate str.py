#Modifying strings

#1.upper
a='   Goerge'
print(a.upper())

#2.lower
print(a.lower())

#remove whitespace with strip()
print(a.strip())

#replace string
print(a.replace('oe','eo'))

#split()
b='george,david'
print(b.split(","))

#concatenation or combine two strings you can use + operator
c='george'
d='david'
e=c+d
print(e)

#to give a space
f= c+' '+d
print(f.upper())

